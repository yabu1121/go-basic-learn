/*
【問題1】成績判定プログラム
点数に応じて成績評価を行うプログラムを作成してください。

【要件】
1. 点数（score）を変数に格納（0〜100）
2. if-else if-elseを使って以下のように評価：
   - 90点以上: "A"
   - 80点以上90点未満: "B"
   - 70点以上80点未満: "C"
   - 60点以上70点未満: "D"
   - 60点未満: "F"
3. switchを使って、評価に応じたコメントを出力：
   - A: "優秀です！"
   - B: "良好です"
   - C: "合格です"
   - D: "もう少し頑張りましょう"
   - F: "不合格です"

【出力例】
========== 成績判定 ==========
点数: 85点
評価: B
コメント: 良好です

【学習ポイント】
- if-else if-elseの使い方
- switchの使い方
- 条件分岐の組み合わせ
*/

func problem1() {
	// ここにコードを書いてください
   var score int = 45
   var rank string
   var comment string
   // ifに()不要
   if score >= 90{
      rank = "A"
   }else if score >= 80 {
      rank = "B"
   }else if score >= 70 {
      rank = "C"
   }else if score >= 60 {
      rank = "D"
   }else{
      rank = "F"
   }

   switch rank{
      case "A": comment = "優秀です！"
      case "B": comment = "良好です"
      case "C": comment = "合格です"
      case "D": comment = "もう少し頑張りましょう"
      case "F": comment = "不合格です"
   }

   fmt.Printf("点数: %d\n", score)
   fmt.Printf("評価: %s\n", rank)
   fmt.Printf("コメント: %s\n", comment)
}

